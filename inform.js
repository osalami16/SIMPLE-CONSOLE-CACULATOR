var opr = prompt("operation")
var num1 = parseInt(prompt("first number 1 :"))
var num2 =parseInt(prompt("second number 2 :"))

if(opr == "+")
{
    alert(num1 + num2 )
}else if(opr == "-")
{
    alert(num1 - num2)
}else if(opr == "*")
{
    alert(num1 * num2)
}else if(opr == "/")
{
    alert(num1 / num2)
}